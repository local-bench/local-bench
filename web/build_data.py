def _has_code_artifacts(items: list[JsonValue]) -> bool:
    return any(
        isinstance(item, dict)
        and item.get("bench") == "bigcodebench_hard"
        and isinstance(item.get("code_artifact"), dict)
        for item in items
    )


def _code_verdict_source(items: list[JsonValue]) -> str | None:
    sources: set[str] = set()
    for item in items:
        if not isinstance(item, dict) or item.get("bench") != "bigcodebench_hard":
            continue
        artifact = item.get("code_artifact")
        if not isinstance(artifact, dict):
            continue
        source = artifact.get("verdict_source")
        if isinstance(source, str):
            sources.add(source)
    if not sources:
        return None
    # Fail closed: a single self-reported ("submitter") coding item taints the whole row's
    # provenance, so it must win over "verifier". Otherwise a mostly-verifier run with one forged
    # "submitter" item would aggregate to "verifier" and slip past _assert_ranked_coding_provenance.
    if "submitter" in sources:
        return "submitter"
    if "verifier" in sources:
        return "verifier"
    return sorted(sources)[0]


