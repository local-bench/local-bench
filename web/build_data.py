@dataclass(frozen=True, slots=True)
class BoardContext:
    headline_lane: str
    models_by_slug: dict[str, JsonObject]


@dataclass(frozen=True, slots=True)
class BuildDataFlags:
    argv: list[str]
    allow_lineage_gaps: bool


def main(argv: list[str] | None = None) -> int:
    try:
        flags = _extract_build_data_flags(sys.argv[1:] if argv is None else argv)
        sources, out_dir, iters, benches, weights = parse_args(flags.argv, root=ROOT, default_iters=DEFAULT_ITERS, default_benches=BENCHES, default_weights=COMPOSITE_WEIGHTS)
        build_static_data(sources, out_dir, iters=iters, benches=benches, weights=weights, allow_lineage_gaps=flags.allow_lineage_gaps)
    except (DataBuildError, OSError, json.JSONDecodeError) as exc:
        print(f"build_data: {exc}", file=sys.stderr)
        return 2
    print(f"wrote {_display_path(out_dir)}")
    # Refresh the EXPERIMENTAL agentic column (a separate, additive read that writes only
    # agentic.json next to the index just built). It never touches index.json/the board, so a
    # single `python web/build_data.py` keeps the agentic column current as new scored runs land.
    # Best-effort: a malformed agentic run must not fail the canonical index build.
    _build_agentic_column(out_dir)
    return 0


def _extract_build_data_flags(argv: list[str]) -> BuildDataFlags:
    filtered: list[str] = []
    allow_lineage_gaps = False
    for arg in argv:
        if arg == "--allow-lineage-gaps":
            allow_lineage_gaps = True
            continue
        filtered.append(arg)
    return BuildDataFlags(argv=filtered, allow_lineage_gaps=allow_lineage_gaps)


def _build_agentic_column(out_dir: Path) -> None:
    """Run web/build_agentic.py's aggregation against the index just written, emitting
    out_dir/agentic.json. Imported lazily and isolated in a try so it cannot affect the
    deterministic index/models/runs outputs or break the build if a run is malformed."""
    try:
        from build_agentic import DEFAULT_RUNS_DIR, build_agentic
        payload = build_agentic(runs_dir=DEFAULT_RUNS_DIR, index_path=out_dir / "index.json")
        agentic_path = out_dir / "agentic.json"
        with agentic_path.open("w", encoding="utf-8", newline="\n") as handle:
            json.dump(payload, handle, indent=2, sort_keys=True)
            handle.write("\n")
        print(f"wrote {_display_path(agentic_path)} ({len(payload['models'])} agentic models)")
    except Exception as exc:  # noqa: BLE001 — agentic is a non-critical preview column
        print(f"build_data: agentic column skipped ({exc})", file=sys.stderr)


def build_static_data(
    sources_path: Path,
    out_dir: Path,
    *,
    iters: int = DEFAULT_ITERS,
    benches: tuple[str, ...] = BENCHES,
    weights: dict[str, float] = COMPOSITE_WEIGHTS,
    allow_lineage_gaps: bool = False,
) -> None:
    sources = [_source(entry, index) for index, entry in enumerate(_list(_read_json(sources_path), "data_sources"))]
    raw_catalog = _read_json(ROOT / "web" / CATALOG_FILENAME)
    catalog = catalog_entries(raw_catalog)
    board = _board_context()
    runs = [_build_run(source, order=index, iters=iters, benches=benches, weights=weights, board=board) for index, source in enumerate(sources)]
    _enforce_integrity_gates(
        runs,
        catalog,
        allow_lineage_gaps=allow_lineage_gaps,
        base_models_by_id=_catalog_base_models_by_id(raw_catalog),
    )
    _write_outputs(out_dir, runs, catalog)


def _board_context() -> BoardContext:
    board = _object(_read_json(BOARD_PATH), str(BOARD_PATH))
    models_by_slug = {
        slug: model
        for model in _list(board.get("models"), f"{BOARD_PATH}:models")
        for model in [_object(model, f"{BOARD_PATH}:model")]
        if (slug := _text(model.get("slug")))
    }
    return BoardContext(
        headline_lane=_string(board.get("lane_scope"), f"{BOARD_PATH}:lane_scope"),
        models_by_slug=models_by_slug,
    )


def _apply_board_intervals(slug: str, run_id: str, axes: JsonObject, composite_interval: JsonObject, board: dict[str, JsonObject]) -> None:
    """For EVERY quant run of a board-ranked model, replace the web build's RE-DERIVED composite
    and headline-axis intervals with the board's verbatim PER-SYSTEM values (matched by run_id).
    board_v2 is the single source of truth: its per-quant composites are ALL on the agentic-led
    Index scale, so stamping every system keeps the leaderboard, the landing scatter, and the
    model-page quant ladder on ONE scale. (Previously only `best_run_id` was stamped -> the best
    variant showed the agentic-led Index while sibling quants kept a re-bootstrapped K+I-only
    composite ~2x higher, so the scatter's best-variant pick diverged from the leaderboard.) Each
    system carries its OWN per-quant composite, so quants still differentiate. Mutates `axes` and
    `composite_interval` in place. No-op for unranked rows and runs with no matching board system."""
    board_model = board.get(slug)
    if board_model is None:
        return
    system = _board_system_for_run(board_model, run_id)
    if system is None:
        return
    composite_interval.update(_board_interval(_object(system.get("composite"), f"board.{slug}.system.composite")))
    board_axes = _object(system.get("axes"), f"board.{slug}.system.axes")
    for axis in _BOARD_AXES:
        if axis not in board_axes:
            continue
        board_axis = _object(board_axes[axis], f"board.{slug}.system.axes.{axis}")
        if axis in axes:
            _object(axes[axis], f"axes.{axis}").update(_board_interval(board_axis))
        else:
            # The web suite run does not compute this axis (agentic comes from the opt-in
            # AppWorld lane, NOT suite-v1), so inject the board's axis object wholesale -> it
            # renders as a first-class axis on the leaderboard, model page, and run receipt.
            axes[axis] = dict(board_axis)


def _board_conformance_gates(slug: str, run_id: str, board: dict[str, JsonObject]) -> JsonObject | None:
    board_model = board.get(slug)
    if board_model is None:
        return None
    system = _board_system_for_run(board_model, run_id)
    if system is None:
        return None
    gates = _object_or_empty(system.get("conformance_gates"))
    return dict(gates) if gates else None


def _quarantine_invalid_inline_appworld(run: JsonObject, source_benches: JsonObject, items: list[JsonValue]) -> list[JsonValue]:
    if _APPWORLD_C_BENCH not in source_benches:
        return []
    agentic_run = run.get("agentic_run")
    if _valid_inline_appworld(agentic_run):
        return []
    source_benches.pop(_APPWORLD_C_BENCH, None)
    items[:] = [
        item
        for item in items
        if not (isinstance(item, dict) and item.get("bench") == _APPWORLD_C_BENCH)
    ]
    return ["agentic appworld_c quarantined: missing diagnostics or harness-dominated client path"]


def _valid_inline_appworld(agentic_run: JsonValue | None) -> bool:
    if not isinstance(agentic_run, dict):
        return False
    diagnostics = agentic_run.get("diagnostics")
    runs = agentic_run.get("runs")
    if not isinstance(diagnostics, dict) or not isinstance(runs, list) or not runs:
        return False
    if _agentic_summary_is_harness_dominated(diagnostics):
        return False
    return not any(not isinstance(run, dict) or _agentic_summary_is_harness_dominated(run) for run in runs)


def _agentic_infra_timeout_warnings(agentic_run: JsonValue | None) -> list[JsonValue]:
    """Fail closed: an inline AppWorld record must prove healthy infrastructure rates.

    A missing, malformed, non-finite, or out-of-range ``infra_timeout_rate`` — in the
    aggregate diagnostics or in any scored run — earns the same warning as an elevated rate.
    New records also gate canonical ``infra_failure_rate`` (timeout + sandbox + transport).
    Older records without the additive canonical field retain the timeout-only gate.
    """
    if not isinstance(agentic_run, dict):
        return []
    summaries: list[JsonValue] = [agentic_run.get("diagnostics")]
    runs = agentic_run.get("runs")
    if isinstance(runs, list):
        summaries.extend(runs)
    elif runs is not None:
        return [_AGENTIC_INFRA_TIMEOUT_REASON]
    if any(not isinstance(summary, dict) or not _infra_rates_healthy(summary) for summary in summaries):
        return [_AGENTIC_INFRA_TIMEOUT_REASON]
    return []


def _infra_timeout_rate_healthy(summary: JsonObject) -> bool:
    return _rate_healthy(summary.get("infra_timeout_rate"))


def _infra_rates_healthy(summary: JsonObject) -> bool:
    if not _infra_timeout_rate_healthy(summary):
        return False
    if "infra_failure_rate" not in summary:
        # Backward compatibility is limited to genuinely older summaries. Once any additive
        # 0.3.1 transport/teardown diagnostic is present, omitting the canonical rate fails closed.
        return not any(
            field in summary
            for field in (
                "transport_failure_count",
                "transport_attempt_count",
                "transport_failure_rate",
                "teardown_failure_count",
                "teardown_failure_rate",
            )
        )
    return _rate_healthy(summary.get("infra_failure_rate"))


def _rate_healthy(value: JsonValue | None) -> bool:
    if isinstance(value, bool) or not isinstance(value, int | float):
        return False
    if not math.isfinite(value) or value < 0.0 or value > 1.0:
        return False
    return value <= _AGENTIC_INFRA_TIMEOUT_RATE_LIMIT


def _agentic_summary_is_harness_dominated(summary: JsonObject) -> bool:
    tasks_total = summary.get("tasks_total")
    tasks_succeeded = summary.get("tasks_succeeded")
    if not isinstance(tasks_total, int) or isinstance(tasks_total, bool) or tasks_total <= 0:
        return False
    if not isinstance(tasks_succeeded, int) or isinstance(tasks_succeeded, bool) or tasks_succeeded != 0:
        return False
    outcome_counts = _object_or_empty(summary.get("outcome_counts"))
    harness_terminal = sum(
        value
        for key in ("cap_exceeded", "no_final_answer", "harness_error")
        if isinstance((value := outcome_counts.get(key)), int) and not isinstance(value, bool)
    )
    return harness_terminal >= tasks_total


def _board_system_for_run(board_model: JsonObject, run_id: str) -> JsonObject | None:
    """The board model's per-quant SYSTEM row whose run_id matches this web run (else None)."""
    for system in _list(board_model.get("systems"), "board.model.systems"):
        system_obj = _object(system, "board.system")
        if _text(system_obj.get("run_id")) == run_id:
            return system_obj
    return None


def _board_interval(interval: JsonObject) -> JsonObject:
    """Just the six interval fields from a board interval object (defensive: ignore any extra
    keys an axis object carries, e.g. n/raw_accuracy, so only the bounds/point are overridden)."""
    return {field: _number(interval.get(field), f"board.interval.{field}") for field in _INTERVAL_FIELDS}


def _board_composites_for_run(
    slug: str,
    run_id: str,
    board: dict[str, JsonObject],
) -> tuple[JsonObject | None, JsonObject | None, str | None]:
    board_model = board.get(slug)
    if board_model is None:
        return None, None, None
    if _board_system_for_run(board_model, run_id) is None:
        return None, None, None
    if run_id not in {_text(board_model.get("best_run_id")), _text(board_model.get("best_system_run_id"))}:
        return None, None, None
    return (
        _board_optional_interval(board_model.get("composite_full")),
        _board_optional_interval(board_model.get("composite_static")),
        _text(board_model.get("static_index_version")),
    )


def _board_optional_interval(value: JsonValue | None) -> JsonObject | None:
    return _board_interval(value) if isinstance(value, dict) else None


def _build_run(source: JsonObject, *, order: int, iters: int, benches: tuple[str, ...], weights: dict[str, float], board: BoardContext) -> JsonObject:
    if _bool(source["demo"], "source.demo"):
        return build_demo_run(source, order=order, benches=benches, index_version=INDEX_VERSION)
    if source.get("file") is None:
        return _build_agentic_only_run(source, order=order)

    path = ROOT / _string(source["file"], "source.file")
    run = _object(_read_json(path), str(path))
    manifest = _object_or_empty(run.get("manifest"))
    suite = _object_or_empty(manifest.get("suite"))
    scorecard = _scorecard_detail(_object_or_empty(manifest.get("scorecard")))
    conformance = _object_or_empty(run.get("conformance"))
    conformance_status = _text(conformance.get("status"))
    contamination_label = _contamination_label(_text(source.get("release_date")))
    source_benches = dict(_object(run.get("benches"), f"{path}:benches"))
    items = list(_list(run.get("items"), f"{path}:items"))
    quarantined_agentic = _quarantine_invalid_inline_appworld(run, source_benches, items)
    agentic_infra_warnings = _agentic_infra_timeout_warnings(run.get("agentic_run"))
    totals = _object(run.get("totals"), f"{path}:totals")
    perf = _object_or_empty(run.get("perf"))
    model_label = _model_label(source, manifest)
    family = _string(source["family"], "source.family")
    kind = _string(source["kind"], "source.kind")
    slug = slugify(model_label)
    run_id = f"{slug}__{path.stem}"
    quant = _text(source.get("quant_label")) or _text(_object_or_empty(manifest.get("model")).get("quant_label"))
    lane = _text(source.get("reasoning_lane")) or _text(suite.get("lane"))
    values, strata, no_answer = scored_inputs(items, metadata.stratum_for_item, source_benches)
    tokens = _completion_token_stats(items)
    est_cost = _number_or_none(run.get("estimated_cost_usd"))
    tok_s = _number_or_none(totals.get("completion_tokens_per_second"))
    latency_s_median = round(tokens["median"] / tok_s, 3) if tokens["median"] is not None and tok_s and tok_s > 0 else None
    summary = _manifest_summary(source, manifest, lane, quant)
    axes, axis_warnings = build_axes(source_benches, values, strata, no_answer, iters, benches)
    composite = build_composite(run, axes, benches, weights)
    # Pure-renderer override: for EVERY quant of a board-ranked model, swap the re-bootstrapped
    # composite and headline-axis intervals for the board's verbatim per-system values (matched
    # by run_id), so the leaderboard, the landing scatter, and the model-page quant ladder all
    # render the board's single agentic-led Index scale. No-op for unranked rows / unmatched
    # runs. See _apply_board_intervals.
    composite_interval = _object(composite["interval"], "composite.interval")
    _apply_board_intervals(slug, run_id, axes, composite_interval, board.models_by_slug)
    conformance_gates = _board_conformance_gates(slug, run_id, board.models_by_slug)
    headline_complete = all(axis in axes for axis in headline_web_axes())
    static_composite = build_composite(run, axes, _STATIC_AXES, _STATIC_WEIGHTS) if _static_complete(axes) else None
    board_composite_full, board_composite_static, board_static_index_version = _board_composites_for_run(slug, run_id, board.models_by_slug)
    computed_composite_full = dict(composite_interval) if headline_complete else None
    computed_composite_static = dict(_object(static_composite["interval"], "static_composite.interval")) if static_composite is not None else None
    composite_full = board_composite_full if board_composite_full is not None else computed_composite_full
    composite_static = board_composite_static if board_composite_static is not None else computed_composite_static
    static_index_version = (board_static_index_version or STATIC_SUITE_INDEX_VERSION) if composite_static is not None else None
    annotations = _source_annotations(source, axes, headline_complete)
    tier = _text(suite.get("tier"))
    rank_eligible_before_infra = (
        tier == "standard"
        and conformance_status == "headline-comparable"
        and headline_complete
        and not quarantined_agentic
    )
    # The fail-closed infra-timeout warning decides RANKING only; it surfaces in
    # data_warnings solely for rows that were otherwise rank-eligible, so demo and
    # diagnostic rows (which often predate the field) keep clean detail pages.
    ranked = rank_eligible_before_infra and not agentic_infra_warnings
    surfaced_infra_warnings = agentic_infra_warnings if rank_eligible_before_infra else []
    score_fields = _score_fields_for_lane(lane, composite_interval, board.headline_lane)
    data_warnings = quarantined_agentic + surfaced_infra_warnings + axis_warnings + _list(composite["warnings"], "composite.warnings")
    if static_composite is not None:
        data_warnings += _list(static_composite["warnings"], "static_composite.warnings")
    detail = {"axes": axes, "composite": composite_interval, "composite_full": composite_full, "composite_static": composite_static, "data_warnings": data_warnings, "est_cost_usd": est_cost, "index_version": INDEX_VERSION, "item_set_hashes": display_item_set_hashes(_object_or_empty(suite.get("item_set_hashes"))), "kind": kind, "conformance": conformance, "contamination_label": contamination_label, "manifest_summary": summary, "model_label": model_label, "ranked": ranked, "run_id": run_id, "scorecard": scorecard, "suite_version": _text(suite.get("suite_version")), "tier": tier, "tokens_to_answer_median": tokens["median"], "tokens_to_answer_p95": tokens["p95"], "totals": totals, "worst_axis": worst_axis(axes, _headline_benches(axes, benches, weights))} | annotations
    if lane != board.headline_lane:
        detail.update(score_fields)
        detail["lane"] = lane
        detail["score_status"] = "measured"
    model_row = {"axes": axes, "composite_full": composite_full, "composite_static": composite_static, "est_cost_usd": est_cost, "file_gb": None, "hardware": _object(summary["hardware"], "summary.hardware"), "lane": lane, "n_errors": _int(totals.get("n_errors"), "totals.n_errors"), "n_items": _int(totals.get("n_items"), "totals.n_items"), "quant_label": quant, "ranked": ranked, "run_id": run_id, "runtime": _object(summary["runtime"], "summary.runtime"), "score_status": "measured", "tier": tier, "tokens_to_answer_median": tokens["median"], "tokens_to_answer_p95": tokens["p95"], "tok_s": tok_s, "latency_s_median": latency_s_median, "vram_footprint_gb": source["vram_footprint_gb"], "vram_required_gb_8k": None, "wall_time_seconds": _number_or_none(totals.get("wall_time_seconds"))} | score_fields | annotations
    index_row = {"axes": axes, "best_run_id": run_id, "gpu": _object(summary["hardware"], "summary.hardware").get("gpu"), "composite_full": composite_full, "composite_static": composite_static, "conformance_status": conformance_status, "contamination_label": contamination_label, "est_cost_usd": est_cost, "family": family, "kind": kind, "lane": lane, "latency_s_median": latency_s_median, "wall_time_seconds": _number_or_none(totals.get("wall_time_seconds")), "model_label": model_label, "n_runs": 1, "ranked": ranked, "replicated": _bool(source["independent_replication"], "source.independent_replication"), "runtime": _object(summary["runtime"], "summary.runtime"), "score_status": "measured", "slug": slug, "tier": tier, "tokens_to_answer_median": tokens["median"], "tokens_to_answer_p95": tokens["p95"]} | score_fields | annotations
    extra_status = _run_status_annotations(run, items)
    detail.update(extra_status)
    model_row.update(extra_status)
    index_row.update(extra_status)
    if static_index_version is not None:
        detail["static_index_version"] = static_index_version
        model_row["static_index_version"] = static_index_version
        index_row["static_index_version"] = static_index_version
    if conformance_gates is not None:
        detail["conformance_gates"] = conformance_gates
        model_row["conformance_gates"] = conformance_gates
        index_row["conformance_gates"] = conformance_gates
    if perf:
        detail["perf"] = perf
        model_row["perf"] = perf
    return {"catalog_id": _text(source.get("model_id")), "composite_raw": composite["raw_point"], "detail": detail, "family": family, "index_row": index_row, "kind": kind, "model_label": model_label, "model_row": model_row, "order": order, "run_id": run_id, "slug": slug, "suite_version": detail["suite_version"]}


