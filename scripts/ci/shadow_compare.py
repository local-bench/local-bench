"""Shadow reproducibility compare: rebuilt board artifacts vs the committed tree.

Run AFTER `localbench.cli board --no-check-parity` and `web/build_data.py` have
regenerated the board + site data in the working tree, and BEFORE those files are
git-restored. Compares the regenerated bytes against the committed (HEAD) bytes and
fails on any difference that is not an explicitly allowlisted provenance field.

Checks
------
1. board_v2.json          normalized compare (allowlist: generated_at,
                          manifest.generated_at, manifest.scorer_git_commit)
2. board_v2.manifest.json normalized compare (allowlist: generated_at,
                          scorer_git_commit). board_sha256 is EXCLUDED from the
                          equality compare but is NEVER ignored: it is verified as a
                          derived-consistency check instead — each side's
                          board_sha256 must equal sha256 of that same side's raw
                          board bytes (preserves the manifest<->board binding).
3. freeze pin             committed board sha256 == committed manifest.board_sha256
                          == web/components/launch-freeze.ts boardSha256 (mirrors the
                          publish gate).
4. web/public/data/**     regenerated bytes must be byte-identical to HEAD
                          (allowlist deliberately empty; any modified/added/deleted
                          file fails).
5. tree scope             no tracked file outside the expected regeneration scope
                          may be modified, and no unexpected untracked file may
                          appear (catches generators writing outside their lane).

Artifacts (--emit-dir)
----------------------
release-manifest.json  commit identity, run-record hashes, input/lockfile hashes,
                       board hashes (raw + normalized), site-data digest, freeze pin,
                       the exact allowlist used, and per-check verdicts. Contains no
                       wall-clock values: identical tree -> identical manifest.
rank-diff.md           board rows at HEAD vs the parent commit's board (needs
                       fetch-depth >= 2; degrades gracefully when unavailable).

Masking note: scorer_git_commit is informational provenance, not a scoring input —
masking it means a tampered commit field alone would not flag; the CONTENT equality
of every scored value is the guarantee this job provides.

Exit codes: 0 = all checks pass, 1 = at least one check failed, 2 = usage/setup error.

Modes:
  shadow_compare.py [--repo-root .] [--emit-dir shadow-ci-out]
  shadow_compare.py --check-normalized-match A/release-manifest.json B/release-manifest.json
      (Job B determinism check: both runs' normalized rebuilt-board hashes must match.)

Stdlib-only by design; the only external process is `git`.
"""

from __future__ import annotations

import argparse
import copy
import hashlib
import json
import re
import subprocess
import sys
from pathlib import Path

BOARD_REL = "cli/runs/board/board_v2.json"
MANIFEST_REL = "cli/runs/board/board_v2.manifest.json"
FREEZE_REL = "web/components/launch-freeze.ts"
SITE_DATA_REL = "web/public/data"
# Every committed run record is a board/site input except cli/runs/board (output).
RUN_RECORD_PATHSPECS = ("runs/bench/", "cli/runs/")
RUN_RECORD_EXCLUDE_PREFIX = "cli/runs/board/"

# The ONLY fields excluded from the normalized equality compare. Any addition to
# these tuples is a reviewed code change, never automatic. board_sha256 carries its
# own derived-consistency check (see verify_manifest_binding).
BOARD_ALLOWLIST: tuple[tuple[str, ...], ...] = (
    ("generated_at",),
    ("manifest", "generated_at"),
    ("manifest", "scorer_git_commit"),
)
MANIFEST_ALLOWLIST: tuple[tuple[str, ...], ...] = (
    ("generated_at",),
    ("scorer_git_commit",),
    ("board_sha256",),  # excluded from equality ONLY because it is re-verified as derived
)

# Tracked paths the rebuild steps are allowed to touch. Anything else dirty = FAIL.
EXPECTED_DIRTY = {BOARD_REL, MANIFEST_REL}
EXPECTED_DIRTY_PREFIXES = (SITE_DATA_REL + "/",)

MASK = "__SHADOW_MASKED__"
MAX_DIFFS_SHOWN = 25


def run_git(repo_root: Path, *args: str) -> bytes:
    proc = subprocess.run(
        ["git", "-C", str(repo_root), *args], capture_output=True, check=False
    )
    if proc.returncode != 0:
        raise RuntimeError(
            f"git {' '.join(args)} failed (exit {proc.returncode}): "
            f"{proc.stderr.decode('utf-8', 'replace').strip()}"
        )
    return proc.stdout


def git_show(repo_root: Path, rev: str, rel_path: str) -> bytes:
    return run_git(repo_root, "show", f"{rev}:{rel_path}")


def sha256_hex(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def mask_fields(obj: object, allowlist: tuple[tuple[str, ...], ...]) -> object:
    """Replace each allowlisted field (when present) with a fixed sentinel.

    A field that is absent is NOT created: a schema change that removes an
    allowlisted field still surfaces as a normal structural diff.
    """
    masked = copy.deepcopy(obj)
    for path in allowlist:
        node = masked
        for key in path[:-1]:
            if not isinstance(node, dict) or key not in node:
                node = None
                break
            node = node[key]
        if isinstance(node, dict) and path[-1] in node:
            node[path[-1]] = MASK
    return masked


def canonical_bytes(obj: object) -> bytes:
    return json.dumps(obj, sort_keys=True, separators=(",", ":"), ensure_ascii=False).encode(
        "utf-8"
    )


def diff_structures(a: object, b: object, path: str = "$") -> list[str]:
    """Recursive structural diff; returns human-readable mismatch lines."""
    diffs: list[str] = []
    if type(a) is not type(b) and not (isinstance(a, (int, float)) and isinstance(b, (int, float))):
        diffs.append(f"{path}: type {type(a).__name__} != {type(b).__name__}")
        return diffs
    if isinstance(a, dict):
        for key in sorted(set(a) | set(b)):
            if key not in a:
                diffs.append(f"{path}.{key}: only in rebuilt")
            elif key not in b:
                diffs.append(f"{path}.{key}: only in committed")
            else:
                diffs.extend(diff_structures(a[key], b[key], f"{path}.{key}"))
    elif isinstance(a, list):
        if len(a) != len(b):
            diffs.append(f"{path}: list length {len(a)} != {len(b)}")
        for i, (x, y) in enumerate(zip(a, b)):
            diffs.extend(diff_structures(x, y, f"{path}[{i}]"))
    elif a != b:
        diffs.append(f"{path}: {a!r} != {b!r}")
    return diffs


class CheckResult:
    def __init__(self, name: str, ok: bool, details: list[str] | None = None):
        self.name = name
        self.ok = ok
        self.details = details or []

    def report(self) -> str:
        status = "PASS" if self.ok else "FAIL"
        lines = [f"[{status}] {self.name}"]
        for d in self.details[:MAX_DIFFS_SHOWN]:
            lines.append(f"    {d}")
        if len(self.details) > MAX_DIFFS_SHOWN:
            lines.append(f"    ... {len(self.details) - MAX_DIFFS_SHOWN} more")
        return "\n".join(lines)


def compare_normalized(
    name: str,
    rebuilt_bytes: bytes,
    committed_bytes: bytes,
    allowlist: tuple[tuple[str, ...], ...],
) -> tuple[CheckResult, str, str]:
    """Returns (result, normalized_rebuilt_sha, normalized_committed_sha)."""
    rebuilt = json.loads(rebuilt_bytes.decode("utf-8"))
    committed = json.loads(committed_bytes.decode("utf-8"))
    rebuilt_masked = mask_fields(rebuilt, allowlist)
    committed_masked = mask_fields(committed, allowlist)
    rebuilt_norm = sha256_hex(canonical_bytes(rebuilt_masked))
    committed_norm = sha256_hex(canonical_bytes(committed_masked))
    if rebuilt_norm == committed_norm:
        return CheckResult(name, True), rebuilt_norm, committed_norm
    diffs = diff_structures(rebuilt_masked, committed_masked)
    if not diffs:
        diffs = ["normalized hashes differ but no structural diff found (serialization issue?)"]
    return CheckResult(name, False, diffs), rebuilt_norm, committed_norm


def verify_manifest_binding(
    name: str, manifest_bytes: bytes, board_bytes: bytes
) -> CheckResult:
    manifest = json.loads(manifest_bytes.decode("utf-8"))
    declared = manifest.get("board_sha256")
    actual = sha256_hex(board_bytes)
    if declared == actual:
        return CheckResult(name, True)
    return CheckResult(
        name, False, [f"manifest board_sha256 {declared!r} != sha256(board bytes) {actual}"]
    )


def check_freeze_pin(repo_root: Path) -> tuple[CheckResult, str]:
    freeze_text = git_show(repo_root, "HEAD", FREEZE_REL).decode("utf-8")
    match = re.search(r'boardSha256:\s*"([0-9a-f]{64})"', freeze_text)
    if not match:
        return CheckResult("freeze-pin", False, [f"boardSha256 pin not found in {FREEZE_REL}"]), ""
    pin = match.group(1)
    committed_board_sha = sha256_hex(git_show(repo_root, "HEAD", BOARD_REL))
    committed_manifest = json.loads(git_show(repo_root, "HEAD", MANIFEST_REL).decode("utf-8"))
    details = []
    if pin != committed_board_sha:
        details.append(f"freeze pin {pin} != committed board sha {committed_board_sha}")
    if committed_manifest.get("board_sha256") != committed_board_sha:
        details.append(
            f"committed manifest board_sha256 {committed_manifest.get('board_sha256')!r}"
            f" != committed board sha {committed_board_sha}"
        )
    return CheckResult("freeze-pin", not details, details), pin


def check_tree_scope(repo_root: Path, emit_dir_rel: str) -> CheckResult:
    porcelain = run_git(repo_root, "status", "--porcelain").decode("utf-8", "replace")
    unexpected: list[str] = []
    site_dirty = 0
    for line in porcelain.splitlines():
        if not line.strip():
            continue
        status, _, rel = line[:2], line[2], line[3:].strip().strip('"')
        rel_slash = rel.replace("\\", "/").rstrip("/")
        if rel_slash == emit_dir_rel or rel_slash.startswith(emit_dir_rel + "/"):
            continue  # our own artifact output
        if rel_slash in EXPECTED_DIRTY:
            continue
        if any(rel_slash.startswith(p) for p in EXPECTED_DIRTY_PREFIXES):
            site_dirty += 1  # judged by the site-data byte compare, not here
            continue
        unexpected.append(f"{status} {rel_slash}")
    details = list(unexpected)
    if site_dirty:
        details.append(
            f"(note: {site_dirty} dirty path(s) under {SITE_DATA_REL} judged by site-data check)"
        )
    return CheckResult("tree-scope", not unexpected, details)


def check_site_data(repo_root: Path) -> CheckResult:
    porcelain = run_git(
        repo_root, "status", "--porcelain", "--", SITE_DATA_REL
    ).decode("utf-8", "replace")
    lines = [ln for ln in porcelain.splitlines() if ln.strip()]
    if not lines:
        return CheckResult("site-data-bytes", True)
    return CheckResult(
        "site-data-bytes",
        False,
        [f"{len(lines)} path(s) not byte-identical to HEAD (allowlist is EMPTY by design):"]
        + lines,
    )


def collect_run_record_hashes(repo_root: Path) -> dict[str, str]:
    listing = run_git(repo_root, "ls-files", "--", *RUN_RECORD_PATHSPECS).decode("utf-8")
    hashes: dict[str, str] = {}
    for rel in sorted(ln.strip() for ln in listing.splitlines() if ln.strip()):
        if rel.startswith(RUN_RECORD_EXCLUDE_PREFIX):
            continue
        hashes[rel] = sha256_hex(git_show(repo_root, "HEAD", rel))
    return hashes


def site_data_digest(repo_root: Path) -> tuple[str, int, str]:
    """Aggregate digest over committed site data: sha256 of 'path:sha' lines."""
    listing = run_git(repo_root, "ls-files", "--", SITE_DATA_REL).decode("utf-8")
    rows = []
    index_sha = ""
    for rel in sorted(ln.strip() for ln in listing.splitlines() if ln.strip()):
        file_sha = sha256_hex(git_show(repo_root, "HEAD", rel))
        rows.append(f"{rel}:{file_sha}")
        if rel == f"{SITE_DATA_REL}/index.json":
            index_sha = file_sha
    agg = sha256_hex("\n".join(rows).encode("utf-8"))
    return agg, len(rows), index_sha


def build_rank_diff(repo_root: Path) -> str:
    def rows_from(board: dict) -> dict[str, dict]:
        return {m["slug"]: m for m in board.get("models", [])}

    def point(m: dict | None) -> float | None:
        comp = (m or {}).get("composite")
        if isinstance(comp, dict):
            return comp.get("point")
        return comp

    def fmt(value: float | None) -> str:
        return f"{value:.2f}" if isinstance(value, (int, float)) else "-"

    head = json.loads(git_show(repo_root, "HEAD", BOARD_REL).decode("utf-8"))
    lines = ["# Board rank diff (HEAD vs parent commit)", ""]
    try:
        parent = json.loads(git_show(repo_root, "HEAD^", BOARD_REL).decode("utf-8"))
    except RuntimeError as exc:
        lines.append(f"Parent board unavailable ({exc}); showing HEAD board only.")
        parent = {"models": []}
    head_rows, parent_rows = rows_from(head), rows_from(parent)
    ordered = sorted(
        head_rows.values(),
        key=lambda m: (point(m) is None, -(point(m) or 0.0)),
    )
    lines += [
        f"HEAD index_version: {head.get('index_version')}  |  "
        f"parent index_version: {parent.get('index_version', 'n/a')}",
        "",
        "| # | slug | composite point (parent -> HEAD) | ranked |",
        "|---|------|----------------------------------|--------|",
    ]
    for i, m in enumerate(ordered, 1):
        slug = m["slug"]
        marker = "" if slug in parent_rows else " (new)"
        lines.append(
            f"| {i} | {slug}{marker} | {fmt(point(parent_rows.get(slug)))} -> "
            f"{fmt(point(m))} | {m.get('ranked')} |"
        )
    for slug in sorted(set(parent_rows) - set(head_rows)):
        lines.append(
            f"| - | {slug} (removed) | {fmt(point(parent_rows[slug]))} -> - | - |"
        )
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--repo-root", default=".")
    parser.add_argument("--emit-dir", default="shadow-ci-out")
    parser.add_argument(
        "--check-normalized-match",
        nargs=2,
        metavar=("MANIFEST_A", "MANIFEST_B"),
        help="Compare normalized rebuilt-board hashes from two release manifests.",
    )
    args = parser.parse_args()

    if args.check_normalized_match:
        a_path, b_path = args.check_normalized_match
        a = json.loads(Path(a_path).read_text(encoding="utf-8"))
        b = json.loads(Path(b_path).read_text(encoding="utf-8"))
        ha = a.get("board", {}).get("normalized_rebuilt_sha256")
        hb = b.get("board", {}).get("normalized_rebuilt_sha256")
        if ha and ha == hb:
            print(f"[PASS] determinism-x2: normalized board hashes match ({ha})")
            return 0
        print(f"[FAIL] determinism-x2: {ha!r} != {hb!r}")
        return 1

    repo_root = Path(args.repo_root).resolve()
    if not (repo_root / BOARD_REL).exists():
        print(f"error: {BOARD_REL} not found under {repo_root}", file=sys.stderr)
        return 2
    emit_dir = (repo_root / args.emit_dir).resolve()
    emit_dir.mkdir(parents=True, exist_ok=True)
    emit_dir_rel = args.emit_dir.replace("\\", "/").rstrip("/")

    rebuilt_board = (repo_root / BOARD_REL).read_bytes()
    rebuilt_manifest = (repo_root / MANIFEST_REL).read_bytes()
    committed_board = git_show(repo_root, "HEAD", BOARD_REL)
    committed_manifest = git_show(repo_root, "HEAD", MANIFEST_REL)

    checks: list[CheckResult] = []

    board_check, board_norm_rebuilt, board_norm_committed = compare_normalized(
        "board-normalized", rebuilt_board, committed_board, BOARD_ALLOWLIST
    )
    checks.append(board_check)

    manifest_check, _, _ = compare_normalized(
        "manifest-normalized", rebuilt_manifest, committed_manifest, MANIFEST_ALLOWLIST
    )
    checks.append(manifest_check)

    checks.append(
        verify_manifest_binding("manifest-binding-rebuilt", rebuilt_manifest, rebuilt_board)
    )
    checks.append(
        verify_manifest_binding("manifest-binding-committed", committed_manifest, committed_board)
    )

    freeze_check, freeze_pin = check_freeze_pin(repo_root)
    checks.append(freeze_check)
    checks.append(check_site_data(repo_root))
    checks.append(check_tree_scope(repo_root, emit_dir_rel))

    head_sha = run_git(repo_root, "rev-parse", "HEAD").decode().strip()
    try:
        parent_sha = run_git(repo_root, "rev-parse", "HEAD^").decode().strip()
    except RuntimeError:
        parent_sha = None

    run_records = collect_run_record_hashes(repo_root)
    data_digest, data_count, index_sha = site_data_digest(repo_root)

    release_manifest = {
        "schema": "local-bench-shadow-release-manifest-v1",
        "commit": head_sha,
        "parent_commit": parent_sha,
        "verdict": "PASS" if all(c.ok for c in checks) else "FAIL",
        "checks": {c.name: ("PASS" if c.ok else "FAIL") for c in checks},
        "board": {
            "committed_sha256": sha256_hex(committed_board),
            "rebuilt_sha256": sha256_hex(rebuilt_board),
            "normalized_committed_sha256": board_norm_committed,
            "normalized_rebuilt_sha256": board_norm_rebuilt,
            "freeze_pin": freeze_pin,
        },
        "allowlist": {
            "board": [".".join(p) for p in BOARD_ALLOWLIST],
            "manifest": [".".join(p) for p in MANIFEST_ALLOWLIST],
            "manifest_board_sha256_note": "excluded from equality, verified as derived consistency",
            "site_data": [],
        },
        "inputs": {
            "run_records": run_records,
            "data_sources_sha256": sha256_hex(git_show(repo_root, "HEAD", "web/data_sources.json")),
            "cli_uv_lock_sha256": sha256_hex(git_show(repo_root, "HEAD", "cli/uv.lock")),
            "web_package_lock_sha256": sha256_hex(
                git_show(repo_root, "HEAD", "web/package-lock.json")
            ),
        },
        "site_data": {
            "file_count": data_count,
            "aggregate_sha256": data_digest,
            "index_json_sha256": index_sha,
        },
        "toolchain": {
            "python": sys.version.split()[0],
            "platform": sys.platform,
        },
    }

    (emit_dir / "release-manifest.json").write_bytes(
        json.dumps(release_manifest, indent=2, sort_keys=True).encode("utf-8") + b"\n"
    )
    (emit_dir / "rank-diff.md").write_bytes(build_rank_diff(repo_root).encode("utf-8"))

    print("shadow_compare: rebuilt artifacts vs committed HEAD "
          f"({head_sha[:9]})\n" + "=" * 60)
    for c in checks:
        print(c.report())
    overall = all(c.ok for c in checks)
    print("=" * 60)
    print(f"OVERALL: {'PASS' if overall else 'FAIL'}  "
          f"(artifacts: {emit_dir / 'release-manifest.json'})")
    return 0 if overall else 1


if __name__ == "__main__":
    sys.exit(main())
