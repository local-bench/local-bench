"""Integration-gate fixture generator (gate-only tooling, not shipped).

Produces the cross-track contract fixture: a COMPLETE 6-axis submission projection
generated by the REAL CLI code path (client_reported_projection) from the REAL Bonsai
bundle, with coding items upgraded to locally-graded form (test-only synthetic verdicts —
this fixture is for validator plumbing, never for publication).

Output: web/tests/fixtures/gate-cli-projection.json  (projection as the CLI emits it)
        web/tests/fixtures/gate-cli-projection.meta.json (bundle sha etc. for the test)

Run from the gate worktree:  cd cli && uv run python ../scripts/gate_fixture_gen.py
"""
from __future__ import annotations

import json
import zipfile
from pathlib import Path

GATE = Path(__file__).resolve().parents[1]
SRC_BUNDLE = Path(r"C:\Users\Michael\.localbench\auto-validator\failed\20260718T195601Z-ticket_31d27669cdcb4498a19e79fc0eaed9a5-772c330e\bundle.lbsub.zip")
SUITE = Path(r"C:\Users\Michael\.cache\localbench\suites\suite-v1-full-exec-6axis-v1\02874cffae45ddcb39688a5651b335a158c4096d148d20062908a382970254cc")
WORK = GATE / "scripts" / "_gate_work"
OUT_DIR = GATE / "web" / "tests" / "fixtures"
PINNED_IMAGE = "bigcodebench/bigcodebench-evaluate@sha256:a3cd34ec3840a49d6b7afb240f4bdd47c350bc5991043fd0a91773830f7cd405"


def synthesize_complete_bundle() -> Path:
    """Copy the real bundle; give every coding item a locally-graded verdict."""
    WORK.mkdir(parents=True, exist_ok=True)
    raw = json.loads(SRC_BUNDLE.read_bytes())
    n_coding = 0
    for item in raw.get("items", []):
        if item.get("bench") == "bigcodebench_hard":
            n_coding += 1
            item["code_artifact"] = {
                "verdict_source": "verifier",
                "verdict": {"passed": (n_coding % 3 == 0)},
                "image_digest": PINNED_IMAGE,
            }
            item["correct"] = n_coding % 3 == 0
    # Mirror what the real grade-coding writer produces: coding axis flips from
    # generated_unverified/verdict_pending to measured/ok once verdicts exist.
    axis_status = raw.get("axis_status", {}).get("axes", {})
    if "coding" in axis_status:
        axis_status["coding"] = {"axis": "coding", "reason": "ok", "status": "measured"}
    out = WORK / "bundle-complete.lbsub.zip"
    out.write_text(json.dumps(raw), encoding="utf-8")
    print(f"synthesized complete bundle: {out} (coding items graded: {n_coding})")
    return out


def main() -> None:
    from localbench.submissions.projection import client_reported_projection

    bundle = synthesize_complete_bundle()
    projection = client_reported_projection(
        bundle,
        suite_dir=SUITE,
        validated_at="2026-07-19T13:00:00Z",
        origin="community",
    )
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    proj_path = OUT_DIR / "gate-cli-projection.json"
    proj_path.write_text(json.dumps(projection, indent=1, sort_keys=True), encoding="utf-8")
    import hashlib
    meta = {
        "raw_bundle_sha256": hashlib.sha256(bundle.read_bytes()).hexdigest(),
        "suite_release_id": projection.get("suite_release_id"),
        "suite_manifest_sha256": projection.get("suite_manifest_sha256"),
        "origin": projection.get("origin"),
        "headline_complete": projection.get("headline_complete"),
        "index_version": projection.get("index_version"),
        "scores": projection.get("scores"),
    }
    (OUT_DIR / "gate-cli-projection.meta.json").write_text(
        json.dumps(meta, indent=1, sort_keys=True), encoding="utf-8",
    )
    print("projection written:", proj_path)
    print("headline_complete:", projection.get("headline_complete"))
    print("index_version:", projection.get("index_version"))
    print("scores:", json.dumps(projection.get("scores"), sort_keys=True))


if __name__ == "__main__":
    main()
